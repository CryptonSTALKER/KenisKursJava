public class Zad3_Point {
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    private int x;
    private int y;

    public Zad3_Point() {
        this.x = x;
        this.y = y;
    }
    public Zad3_Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
